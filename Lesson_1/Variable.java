class Variable {
    public static void main (String[] args){
        byte num_core = 8;
        short proc = 3400;
        int memory = 8000;
        long l = 1253657L;
        float f = 3.14F;
        double proc2 = 3.40;
        char c = '#';
        boolean b = true;
        System.out.println("Num_core = " + num_core);
        System.out.println("Proc = " + proc);
        System.out.println("Memory = " + memory);
        System.out.println("Long var = " + l);
        System.out.println("Processor = " + proc2);
        System.out.println("Char = " + c);
        System.out.println("Boolean var = " + b);

    }
}